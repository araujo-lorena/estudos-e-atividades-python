# TIPAGEM DINÂMICA

"""A tipagem dinâmica permite que variáveis mudem de tipo durante a execução
 do programa.O erro ocorrerá na execução, caso uma operação inválida seja realizada."""

"""Exemplo:
x = 10        # x é um inteiro
x = "Olá"     # agora x é uma string """

# TIPAGEM FORTE

"""A tipagem forte significa que uma variável não pode mudar de tipo implicitamente.O 
erro será apontado na execução, caso uma operação inválida seja realizada."""

"""Exemplo:
x = 10        # x é um inteiro
x = "Olá"     # agora x é uma string 
y = x + 5     # Isso causará um erro, pois não se pode somar uma string com um inteiro.
"""